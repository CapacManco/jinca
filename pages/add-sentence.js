import React, { useState } from 'react';
import Head from 'next/head';
import styles from '../styles/Layout.module.css';

import db from '../utils/firebaseConfig';
import { collection, addDoc, getDocs } from 'firebase/firestore';

const Contact = () => {
  const [formData, setFormData] = useState({ quote: '', author: '' });
  const [formSent, setFormSent] = useState(false);

  const sendInfos = async (e) => {
    e.preventDefault();

    if (!formData.quote || !formData.author) return;
    try {
      const docRef = await addDoc(collection(db, 'phrases'), {
        quote: formData.quote,
        author: formData.author,
        date: new Date().toLocaleString(),
      });
      setFormSent(true);
      console.log('Document written with ID: ', docRef.id);
    } catch (e) {
      console.error('Error adding document: ', e);
    }
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>Confidence Bot</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
        <h1>Propose a sentence</h1>
        <p>Feel free to propose your own motivational sentence.</p>
        <p>Don't forget to include the author.</p>
        <form className={styles.form}>
          <label>Phrase</label>
          <input
            className={styles.form__input}
            value={formData.quote}
            onChange={(e) =>
              setFormData({ quote: e.target.value, author: formData.author })
            }
            required
          />
          <label>Author</label>
          <input
            className={styles.form__input}
            value={formData.author}
            onChange={(e) =>
              setFormData({ quote: formData.quote, author: e.target.value })
            }
            required
          />
          <button className={styles.form__button} onClick={(e) => sendInfos(e)}>
            Send
          </button>
        </form>
        {formSent ? (
          <p className={styles.form__info}>Your sentence has been sent !</p>
        ) : (
          ''
        )}
      </main>
    </div>
  );
};

export default Contact;
